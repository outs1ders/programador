// operadores basicos
3 + 4 //7
50 - 10 //40
10 * 10 //100
20 / 2 //10

// operadores con tipos de dato
"Dego" + "ogber" // Degoober 

// operadores unitarios
!false // true

// operadores de asignacion
var a = 1;

// operadores de comparacion
3 == "3"; //true
3 === "3"; //false
5 < 3; //false
5 > 3; //true
5 <= 6; //true
5 >= 6; //false

// operadores booleanos
a && b // True, las dos son verdaderas (and)
a || b // True, alguna de las dos es verdadera (or)

// operadores de aumento y sustraccion
a++ // a + 1
a += 3 // a + 3
a-- // a - 1
a -= 5 // a + 5

